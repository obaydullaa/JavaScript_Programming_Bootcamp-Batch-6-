// For Loop

const str = 'Allah Mohan';

for (let i = 0; i < str.length; i++) {
    console.log(str[i]);
}

// While Loop
const text = 'Allag Mohan';
let count = 0;

while (count < text.length) {
    console.log(text[count]);
    count++;
}