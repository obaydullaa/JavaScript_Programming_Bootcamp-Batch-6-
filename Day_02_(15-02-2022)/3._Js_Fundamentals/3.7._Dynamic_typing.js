// Dynamic Typing

// অন্যান্য প্রোগ্রামিং এ ভ্যারিয়েবল নেমটা ডাটা টাইপ কন্ট্রোল করে, কিন্তু জাভাস্ক্রিপ্ট এ ভ্যালু নিজেই কন্ট্রোল করে। তাই একে ডাইনামিক ডাটা টাইপ বলা হয় ।

let productName = 'Potato';

console.log(typeof(productName));

productName = true;
let productNameInt = 7;

