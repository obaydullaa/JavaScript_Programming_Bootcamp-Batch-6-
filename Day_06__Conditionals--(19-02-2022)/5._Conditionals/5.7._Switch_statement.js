/**
 * 5.7 Switch Statement
 */


const product = 'Rode';

switch (product) {
    case 'Blue Yeti':
        console.log('Better Microphone');
        break;
    case 'Snowball':
        console.log('Good Microphone');
        break;
    case 'Rode':
        console.log('Ok');
        break;
    default:
        console.log('Some unknown microphone');
}